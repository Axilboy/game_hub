/**
 * Развёрнутые тексты для витрины магазина (вводное + что входит).
 */

const GAME_TITLE = {
  spy: 'Шпион',
  mafia: 'Мафия',
  elias: 'Элиас',
  truth_dare: 'Правда или действие',
  bunker: 'Бункер',
};

/**
 * @returns {{ intro: string, bullets: string[], note?: string }}
 */
export function getShopItemMarketing(item) {
  const gameTitle = GAME_TITLE[item.game] || item.game;

  if (item.game === 'spy' && item.category === 'dictionaries') {
    if (item.id === 'spy_free') {
      return {
        intro:
          'Базовый словарь локаций для «Шпиона» — стартовый набор классических мест: офис, кафе, транспорт и другие знакомые ситуации. Подходит для первой партии и обучения новичков.',
        bullets: [
          'Подключается в лобби при выборе словаря.',
          'Бесплатно для всех в комнате.',
          'Сочетается с таймером, голосованием и ролями GameHub.',
        ],
        note: 'Премиум-наборы добавляют тематику: детектив, путешествия, кино и др.',
      };
    }
    return {
      intro: `Тематический словарь «${item.name}» для игры «Шпион»: подборка локаций и слов в выбранном стиле (${item.description.replace(/\.\s*Премиум\.?\s*$/i, '').trim()}). Мирные знают место и слово, шпион — нет; ответы наводят на отгадку без прямого называния.`,
      bullets: [
        'В лобби словарь выбирается перед стартом — можно комбинировать с другими доступными наборами.',
        'Больше разнообразия за вечер: меньше повторов одних и тех же локаций.',
        'Работает вместе с голосованием, таймером и словарями комнаты.',
      ],
      note: 'При активной подписке Премиум премиальные словари доступны всей группе в этой комнате.',
    };
  }

  if (item.game === 'elias' && item.category === 'dictionaries') {
    if (item.free) {
      return {
        intro: `Словарь «${item.name}» для «Элиаса»: слова для объяснения команде по одному слову за раунд. ${item.description}`,
        bullets: [
          'Выбирается в лобби при настройке матча.',
          'Подходит для тренировки и коротких партий.',
          'Совместим с таймером и подсчётом очков GameHub.',
        ],
      };
    }
    return {
      intro: `Тематический словарь «${item.name}» для «Элиаса»: расширенный набор слов (${item.description.replace(/\.\s*Премиум\.?\s*$/i, '').trim()}). Ведущий видит слово, команда — нет; нужно объяснить без прямых подсказок.`,
      bullets: [
        'Подключается в лобби к базовым наборам.',
        'Добавляет глубину и разнообразие формату вечеринки.',
        'Премиум открывает словарь для всех участников комнаты на сессию.',
      ],
    };
  }

  if (item.id === 'mafia_extended') {
    return {
      intro:
        'Расширенный режим «Мафии» в GameHub: дополнительные роли (Дон, Комиссар Каратель, Доктор, Маньяк и др.) — больше стратегии и неожиданных поворотов, чем в классике.',
      bullets: [
        'Настраивается в лобби до старта партии.',
        'Фазы дня/ночи, голосования и подсказки — как в приложении.',
        'Доступен при Премиуме или отдельной разблокировке (как в витрине).',
      ],
    };
  }

  if (item.game === 'truth_dare' && item.category === 'packs') {
    return {
      intro: `Пак карточек для «Правда или действие»: ${item.description.replace(/\.\s*Премиум\.?\s*$/i, '').trim()}. Карточки подмешиваются к общему пулу категорий в комнате.`,
      bullets: [
        'Включается в лобби при выборе паков и категорий.',
        'Можно сочетать с безопасным (safe) режимом там, где это уместно.',
        'Удобно для больших компаний и тематических вечеров.',
      ],
      note: 'Премиум открывает паки для всех в комнате.',
    };
  }

  if (item.game === 'bunker' && item.category === 'scenarios') {
    return {
      intro: `Сценарий для «Бункера»: ${item.description.replace(/\.\s*Премиум\.?\s*$/i, '').trim()}. Задаёт кризис, условия отбора и атмосферу обсуждения.`,
      bullets: [
        'Выбирается при создании или настройке комнаты.',
        'Влияет на характеристики персонажей и голосования.',
        'Расширяет переигрываемость за счёт новых условий.',
      ],
    };
  }

  if (item.game === 'bunker' && item.category === 'packs') {
    return {
      intro: `Пак «Бункер»: ${item.description.replace(/\.\s*Премиум.*$/i, '').trim()}.`,
      bullets: [
        'Дополнительные строки на карточке — больше споров и блефа.',
        'Доступно владельцу пака или подписчику Pro.',
        'Раскрываются по очереди вместе с базовыми полями.',
      ],
    };
  }

  return {
    intro: `Дополнение для «${gameTitle}». ${item.description}`,
    bullets: [
      'Подключается в лобби GameHub при настройке комнаты.',
      'После разблокировки доступно в этой сессии участникам согласно правилам витрины.',
      'Премиум распространяется на всю комнату там, где это указано.',
    ],
  };
}
